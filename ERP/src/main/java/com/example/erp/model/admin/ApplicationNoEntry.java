package com.example.erp.model.admin;

public class ApplicationNoEntry {

	
	private int id;
	private String academicYear;
	private String programType;
	private String program;
	private String course;
	private int onlineapplicationNoFrom;
	private int onlineapplicationNoto;
	private int offlineapplicationNoFrom;
	private int offlineapplicationNoto;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAcademicYear() {
		return academicYear;
	}
	public void setAcademicYear(String academicYear) {
		this.academicYear = academicYear;
	}
	public String getProgramType() {
		return programType;
	}
	public void setProgramType(String programType) {
		this.programType = programType;
	}
	public String getProgram() {
		return program;
	}
	public void setProgram(String program) {
		this.program = program;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public int getOnlineapplicationNoFrom() {
		return onlineapplicationNoFrom;
	}
	public void setOnlineapplicationNoFrom(int onlineapplicationNoFrom) {
		this.onlineapplicationNoFrom = onlineapplicationNoFrom;
	}
	public int getOnlineapplicationNoto() {
		return onlineapplicationNoto;
	}
	public void setOnlineapplicationNoto(int onlineapplicationNoto) {
		this.onlineapplicationNoto = onlineapplicationNoto;
	}
	public int getOfflineapplicationNoFrom() {
		return offlineapplicationNoFrom;
	}
	public void setOfflineapplicationNoFrom(int offlineapplicationNoFrom) {
		this.offlineapplicationNoFrom = offlineapplicationNoFrom;
	}
	public int getOfflineapplicationNoto() {
		return offlineapplicationNoto;
	}
	public void setOfflineapplicationNoto(int offlineapplicationNoto) {
		this.offlineapplicationNoto = offlineapplicationNoto;
	}
	
	
	
}
