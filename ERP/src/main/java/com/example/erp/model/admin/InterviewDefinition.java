package com.example.erp.model.admin;

public class InterviewDefinition {

	private int id;
	private String admitted_year;
	private String programType;
	private String program;
	private String course;
	private String interviewType;
	private String sequenceOfInterview;
	private String interviewPerPanel;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAdmitted_year() {
		return admitted_year;
	}
	public void setAdmitted_year(String admitted_year) {
		this.admitted_year = admitted_year;
	}
	public String getProgramType() {
		return programType;
	}
	public void setProgramType(String programType) {
		this.programType = programType;
	}
	public String getProgram() {
		return program;
	}
	public void setProgram(String program) {
		this.program = program;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getInterviewType() {
		return interviewType;
	}
	public void setInterviewType(String interviewType) {
		this.interviewType = interviewType;
	}
	public String getSequenceOfInterview() {
		return sequenceOfInterview;
	}
	public void setSequenceOfInterview(String sequenceOfInterview) {
		this.sequenceOfInterview = sequenceOfInterview;
	}
	public String getInterviewPerPanel() {
		return interviewPerPanel;
	}
	public void setInterviewPerPanel(String interviewPerPanel) {
		this.interviewPerPanel = interviewPerPanel;
	}
	
}
